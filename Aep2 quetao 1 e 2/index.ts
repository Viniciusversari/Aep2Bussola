


class matemagica {

    static adicao(a , b ) {
      return a + b;
    }
  
    static subtracao(a, b) {
      return a - b;
    }
  
    static multiplicacao(a, b) {
      return a * b;
    }
  
    static divisao(a, b) {
      return a / b;
    }

  }

  module.exports = matemagica;
  







